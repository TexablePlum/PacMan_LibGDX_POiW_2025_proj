package io.github.PacMan.Menu;

/**
 * Enum reprezentujący typ wyrównania elementów menu.
 *
 * <p>Definiuje trzy możliwości wyrównania:
 * <ul>
 *   <li>{@code LEFT} – wyrównanie do lewej strony,</li>
 *   <li>{@code CENTER} – wyśrodkowanie,</li>
 *   <li>{@code RIGHT} – wyrównanie do prawej strony.</li>
 * </ul>
 * </p>
 */

public enum AlignmentType {
    LEFT,
    RIGHT,
    CENTER,
}
